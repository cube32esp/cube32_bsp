static const size_t lsm6dsox_activity_recognition_ucf_size =
    sizeof(lsm6dsox_activity_recognition_ucf) / sizeof(ucf_line_t);

// ============================================================================
// Activity class mapping (MLC output values are non-sequential)
// ============================================================================

#define LSM6DSOX_MLC_ACTIVITY_STATIONARY  0
#define LSM6DSOX_MLC_ACTIVITY_WALKING     1
#define LSM6DSOX_MLC_ACTIVITY_JOGGING     4
#define LSM6DSOX_MLC_ACTIVITY_BIKING      8
#define LSM6DSOX_MLC_ACTIVITY_DRIVING     12

/**
 * Map MLC output value to activity label string.
 */
static inline const char* lsm6dsox_activity_label(uint8_t mlc_out) {
    switch (mlc_out) {
        case LSM6DSOX_MLC_ACTIVITY_STATIONARY: return "Stationary";
        case LSM6DSOX_MLC_ACTIVITY_WALKING:    return "Walking";
        case LSM6DSOX_MLC_ACTIVITY_JOGGING:    return "Jogging";
        case LSM6DSOX_MLC_ACTIVITY_BIKING:     return "Biking";
        case LSM6DSOX_MLC_ACTIVITY_DRIVING:    return "Driving";
        default:                                return "Unknown";
    }
}

/**
 * Check if MLC output is a known activity class.
 */
static inline bool lsm6dsox_activity_is_known(uint8_t mlc_out) {
    switch (mlc_out) {
        case LSM6DSOX_MLC_ACTIVITY_STATIONARY:
        case LSM6DSOX_MLC_ACTIVITY_WALKING:
        case LSM6DSOX_MLC_ACTIVITY_JOGGING:
        case LSM6DSOX_MLC_ACTIVITY_BIKING:
        case LSM6DSOX_MLC_ACTIVITY_DRIVING:
            return true;
        default:
            return false;
    }
}

